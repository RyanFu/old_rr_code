package com.xiaonei.reg.activity.recallbonus.model;

public class RecallBonusAwardModel {

	private int id;
	private int uid;
	private int award_id;
	private String award_time;
	private String award_remark;
	private String award_get_time;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getUid() {
		return uid;
	}
	public void setUid(int uid) {
		this.uid = uid;
	}
	public int getAward_id() {
		return award_id;
	}
	public void setAward_id(int award_id) {
		this.award_id = award_id;
	}
	public String getAward_time() {
		return award_time;
	}
	public void setAward_time(String award_time) {
		this.award_time = award_time;
	}
	public String getAward_remark() {
		return award_remark;
	}
	public void setAward_remark(String award_remark) {
		this.award_remark = award_remark;
	}
	public String getAward_get_time() {
		return award_get_time;
	}
	public void setAward_get_time(String award_get_time) {
		this.award_get_time = award_get_time;
	}

	
}
