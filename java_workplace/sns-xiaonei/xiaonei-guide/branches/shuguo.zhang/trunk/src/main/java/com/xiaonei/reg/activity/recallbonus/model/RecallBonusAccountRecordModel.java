package com.xiaonei.reg.activity.recallbonus.model;

public class RecallBonusAccountRecordModel {
	private int id;
	private int uid;
	private int act_type;
	private int act_amount;
	private String act_time;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getUid() {
		return uid;
	}
	public void setUid(int uid) {
		this.uid = uid;
	}
	public int getAct_type() {
		return act_type;
	}
	public void setAct_type(int act_type) {
		this.act_type = act_type;
	}
	public int getAct_amount() {
		return act_amount;
	}
	public void setAct_amount(int act_amount) {
		this.act_amount = act_amount;
	}
	public String getAct_time() {
		return act_time;
	}
	public void setAct_time(String act_time) {
		this.act_time = act_time;
	}
	
}
